### Membership Operators ###

# in
# Returns True if the specified value is found in the sequence (list, string, etc.)
x = ["apple", "Mango", "Orange"]
print("banana" in x)  # Output: False ("banana" is not in the list)

# not in
# Returns True if the specified value is not found in the sequence
x = ["apple", "banana", "Mango", "Orange"]
print("pineapple" not in x)  # Output: True ("pineapple" is not in the list)
