### Comparison Operators ###

# == 
# Checks if the left and right values are equal
x = 7
y = 2
print(x == y)  # Output: False (7 is not equal to 2)

# !=
# Checks if the left and right values are not equal
x = 8
y = 1
print(x != y)  # Output: True (8 is not equal to 1)

# >
# Checks if the left value is greater than the right value
x = 5
y = 4
print(x > y)  # Output: True (5 is greater than 4)

# <
# Checks if the left value is less than the right value
x = 7
y = 4
print(x < y)  # Output: False (7 is not less than 4)

# >=
# Checks if the left value is greater than or equal to the right value
x = 5
y = 6
print(x >= y)  # Output: False (5 is not greater than or equal to 6)

# <=
# Checks if the left value is less than or equal to the right value
x = 5
y = 3
print(x <= y)  # Output: False (5 is not less than or equal to 3)
