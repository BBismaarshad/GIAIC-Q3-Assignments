import math

# Constants
print(math.pi)      # 3.141592653589793
print(math.e)       # 2.718281828459045

# Basic functions
print(math.sqrt(16))      # 4.0 (square root)
print(math.pow(2, 3))     # 8.0 (2 to the power of 3)
print(math.log(10))       # 2.302585092994046 (natural logarithm)

# Trigonometric functions
print(math.sin(math.pi/2))  # 1.0 (sine of 90 degrees)
print(math.cos(math.pi))    # -1.0 (cosine of 180 degrees)

# Rounding functions
print(math.ceil(4.2))      # 5 (round up)
print(math.floor(4.9))     # 4 (round down)

# Other useful functions
print(math.factorial(5))   # 120 (5!)
print(math.gcd(12, 18))    # 6 (greatest common divisor)